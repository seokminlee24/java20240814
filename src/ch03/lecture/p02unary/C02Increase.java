package ch03.lecture.p02unary;

public class C02Increase {
    public static void main(String[] args) {
        //증가연산자
        int a = 1;
        ++a;
        System.out.println("a = " + a); //2
        
        ++a;
        System.out.println("a = " + a);//3
        
        a++;
        System.out.println("a = " + a);//4
    }
}
