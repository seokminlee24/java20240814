package ch04.lecture.p01if;

public class C01If {
    public static void main(String[] args) {
        //if(조건문, 분기문(branch))
        System.out.println(1);

        if(false){
            // 조건에 의해실행 되거나
            //실행되 않도록 하고 싶은 코드들
            //true 실행
            //false 시 실행 안됨
            System.out.println(2);
            System.out.println(3);
        }
        System.out.println(4);
        System.out.println(5);
        System.out.println(6);
    }
}
