package ch06.sol.s13;

public class Printer {
    private int a;
    private boolean b;
    private double c;
    private String name;

    public void setA(int a) {
        this.a = a;
    }

    public void setB(boolean b) {
        this.b = b;
    }

    public void setC(double c) {
        this.c = c;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getA() {
        return a;
    }

    public boolean isB() {
        return b;
    }

    public double getC() {
        return c;
    }

    public String getName() {
        return name;
    }
}
